async function getTemplateByName(name) {
  const template = await strapi.db.query('api::specification-template.specification-template').findOne({
    where: { name },
  });

  if (!template) {
    throw new Error(`Не найден шаблон характеристик: ${name}`);
  }

  return template;
}

async function getCategoriesByName(name) {
  return strapi.db.query('api::moysklad-category.moysklad-category').findMany({
    where: { name },
    select: ['id', 'documentId', 'name', 'pathName'],
    limit: 50,
  });
}

async function updateCategoryTemplate(category, template) {
  await strapi.db.query('api::moysklad-category.moysklad-category').update({
    where: { id: category.id },
    data: {
      defaultSpecificationTemplate: template.id,
    },
  });
}

async function seedCategorySpecificationTemplates() {
  let updatedCount = 0;

  for (const [categoryName, templateName] of CATEGORY_TEMPLATE_MAPPINGS) {
    const template = await getTemplateByName(templateName);
    const categories = await getCategoriesByName(categoryName);

    if (!categories.length) {
      console.warn(`Категория не найдена: ${categoryName}`);
      continue;
    }

    for (const category of categories) {
      await updateCategoryTemplate(category, template);
      updatedCount += 1;

      console.log(
        `Обновлено: ${category.name} → ${template.name} | ${category.pathName || ''}`
      );
    }
  }

  console.log(`Готово: привязано категорий к шаблонам: ${updatedCount}.`);
}

async function main() {
  const { createStrapi, compileStrapi } = require('@strapi/strapi');

  const appContext = await compileStrapi();
  const app = await createStrapi(appContext).load();

  app.log.level = 'error';

  try {
    await seedCategorySpecificationTemplates();
  } finally {
    await app.destroy();
  }

  process.exit(0);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
